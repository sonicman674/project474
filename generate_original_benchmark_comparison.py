"""
generate_original_benchmark_comparison.py
=========================================

Compares this project's reproduced Anomaly Transformer results on the four
original benchmark datasets available in this repo against the ICLR 2022 paper.

Datasets compared:
  SMD, MSL, SMAP, PSM

SWaT is not included because it is not one of the locally downloaded/rerun
datasets in the notebook.

Outputs are saved to:
  original_benchmark_comparison/

Run:
  python generate_original_benchmark_comparison.py
"""

import csv
import os

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np


BASE_DIR = os.path.dirname(os.path.abspath(__file__))
OUT_DIR = os.path.join(BASE_DIR, "original_benchmark_comparison")
os.makedirs(OUT_DIR, exist_ok=True)


def save(name):
    plt.savefig(os.path.join(OUT_DIR, f"{name}.pdf"), bbox_inches="tight")
    plt.savefig(os.path.join(OUT_DIR, f"{name}.png"), dpi=150, bbox_inches="tight")
    plt.close()
    print(f"Saved -> original_benchmark_comparison/{name}.pdf / .png")


# Paper values: Anomaly Transformer ICLR 2022, Table 1.
PAPER_RESULTS = {
    "SMD":  {"P": 89.40, "R": 95.45, "F1": 92.33},
    "MSL":  {"P": 92.09, "R": 95.15, "F1": 93.59},
    "SMAP": {"P": 94.13, "R": 99.40, "F1": 96.69},
    "PSM":  {"P": 96.91, "R": 98.90, "F1": 97.89},
}


DATASETS = list(PAPER_RESULTS.keys())
METRICS = ["P", "R", "F1"]
METRIC_LABELS = {"P": "Precision", "R": "Recall", "F1": "F1-score"}


def load_reproduced_results():
    """Load reproduced metrics from test_outputs generated by scripts/*.sh."""
    results = {}
    for dataset in DATASETS:
        path = os.path.join(BASE_DIR, "test_outputs", f"{dataset}_test_outputs.npz")
        if not os.path.exists(path):
            raise FileNotFoundError(
                f"Missing reproduced output for {dataset}: {path}\n"
                f"Run bash ./scripts/{dataset}.sh first to generate it."
            )
        output = np.load(path)
        results[dataset] = {
            "P": float(output["precision"]) * 100,
            "R": float(output["recall"]) * 100,
            "F1": float(output["f_score"]) * 100,
        }
    return results


def write_comparison_csv():
    our_results = load_reproduced_results()
    path = os.path.join(OUT_DIR, "original_benchmark_comparison.csv")
    with open(path, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow([
            "Dataset",
            "Metric",
            "Paper (%)",
            "Reproduced (%)",
            "Delta reproduced - paper (points)",
        ])
        for dataset in DATASETS:
            for metric in METRICS:
                paper = PAPER_RESULTS[dataset][metric]
                ours = our_results[dataset][metric]
                writer.writerow([
                    dataset,
                    METRIC_LABELS[metric],
                    f"{paper:.2f}",
                    f"{ours:.2f}",
                    f"{ours - paper:+.2f}",
                ])
    print(f"Saved -> original_benchmark_comparison/original_benchmark_comparison.csv")


def fig1_f1_comparison():
    our_results = load_reproduced_results()
    paper_f1 = [PAPER_RESULTS[d]["F1"] for d in DATASETS]
    our_f1 = [our_results[d]["F1"] for d in DATASETS]

    x = np.arange(len(DATASETS))
    width = 0.36

    fig, ax = plt.subplots(figsize=(8.6, 5.0))
    bars_paper = ax.bar(
        x - width / 2,
        paper_f1,
        width,
        color="#5b7fbe",
        alpha=0.88,
        label="ICLR 2022 paper",
        zorder=3,
    )
    bars_ours = ax.bar(
        x + width / 2,
        our_f1,
        width,
        color="#c44e52",
        alpha=0.88,
        label="This reproduction",
        zorder=3,
    )

    for bars in [bars_paper, bars_ours]:
        for bar in bars:
            value = bar.get_height()
            ax.text(
                bar.get_x() + bar.get_width() / 2,
                value + 0.35,
                f"{value:.2f}",
                ha="center",
                va="bottom",
                fontsize=8.5,
            )

    ax.set_xticks(x)
    ax.set_xticklabels(DATASETS)
    ax.set_ylabel("F1-score (%)")
    ax.set_ylim(85, 101)
    ax.set_title(
        "Original Benchmark Reproduction: F1-score Comparison\n"
        "Paper Table 1 vs this project's rerun",
        pad=10,
    )
    ax.yaxis.grid(True, linestyle="--", linewidth=0.5, alpha=0.6, zorder=0)
    ax.set_axisbelow(True)
    ax.spines[["top", "right"]].set_visible(False)
    ax.legend(frameon=False, loc="lower center", bbox_to_anchor=(0.5, -0.20), ncol=2)

    plt.tight_layout(rect=[0, 0.08, 1, 1])
    save("fig1_original_f1_comparison")


def fig2_metric_deltas():
    our_results = load_reproduced_results()
    fig, axes = plt.subplots(1, 3, figsize=(12.5, 4.2), sharey=True)
    colors = {"P": "#1f77b4", "R": "#ff7f0e", "F1": "#2ca02c"}

    for ax, metric in zip(axes, METRICS):
        deltas = [our_results[d][metric] - PAPER_RESULTS[d][metric] for d in DATASETS]
        bar_colors = ["#2ca02c" if d >= 0 else "#c44e52" for d in deltas]
        bars = ax.bar(DATASETS, deltas, color=bar_colors, alpha=0.88, zorder=3)
        ax.axhline(0, color="#333333", linewidth=0.9)
        for bar, delta in zip(bars, deltas):
            va = "bottom" if delta >= 0 else "top"
            y = delta + 0.12 if delta >= 0 else delta - 0.12
            ax.text(
                bar.get_x() + bar.get_width() / 2,
                y,
                f"{delta:+.2f}",
                ha="center",
                va=va,
                fontsize=8.5,
            )
        ax.set_title(f"{METRIC_LABELS[metric]} delta", color=colors[metric], fontsize=11)
        ax.set_xlabel("Dataset")
        ax.yaxis.grid(True, linestyle="--", linewidth=0.4, alpha=0.5, zorder=0)
        ax.set_axisbelow(True)
        ax.spines[["top", "right"]].set_visible(False)

    axes[0].set_ylabel("Reproduced - paper (percentage points)")
    fig.suptitle(
        "Original Benchmark Reproduction Error\n"
        "Positive values mean this rerun exceeded the paper value",
        fontsize=12,
        fontweight="bold",
        y=1.02,
    )
    plt.tight_layout()
    save("fig2_original_metric_deltas")


def fig3_full_metric_comparison():
    our_results = load_reproduced_results()
    fig, axes = plt.subplots(1, 4, figsize=(14, 4.4), sharey=True)
    x = np.arange(len(METRICS))
    width = 0.36

    for ax, dataset in zip(axes, DATASETS):
        paper_vals = [PAPER_RESULTS[dataset][m] for m in METRICS]
        our_vals = [our_results[dataset][m] for m in METRICS]
        ax.bar(
            x - width / 2,
            paper_vals,
            width,
            color="#5b7fbe",
            alpha=0.88,
            label="Paper",
            zorder=3,
        )
        ax.bar(
            x + width / 2,
            our_vals,
            width,
            color="#c44e52",
            alpha=0.88,
            label="Reproduced",
            zorder=3,
        )
        ax.set_xticks(x)
        ax.set_xticklabels([METRIC_LABELS[m] for m in METRICS], rotation=20)
        ax.set_title(dataset, fontsize=11)
        ax.set_ylim(80, 103)
        ax.yaxis.grid(True, linestyle="--", linewidth=0.4, alpha=0.5, zorder=0)
        ax.set_axisbelow(True)
        ax.spines[["top", "right"]].set_visible(False)

    axes[0].set_ylabel("Score (%)")
    handles, labels = axes[0].get_legend_handles_labels()
    fig.legend(
        handles,
        labels,
        loc="lower center",
        ncol=2,
        frameon=False,
        bbox_to_anchor=(0.5, 0.02),
    )
    fig.suptitle(
        "Original Benchmark Reproduction: Precision, Recall, and F1",
        fontsize=12,
        fontweight="bold",
        y=0.98,
    )
    plt.tight_layout(rect=[0, 0.10, 1, 0.90])
    save("fig3_original_prf1_comparison")


def fig4_results_table():
    our_results = load_reproduced_results()
    headers = [
        "Dataset",
        "Paper P",
        "Our P",
        "Delta P",
        "Paper R",
        "Our R",
        "Delta R",
        "Paper F1",
        "Our F1",
        "Delta F1",
    ]
    rows = []
    for dataset in DATASETS:
        row = [dataset]
        for metric in METRICS:
            paper = PAPER_RESULTS[dataset][metric]
            ours = our_results[dataset][metric]
            row.extend([f"{paper:.2f}", f"{ours:.2f}", f"{ours - paper:+.2f}"])
        rows.append(row)

    fig, ax = plt.subplots(figsize=(12.8, 2.8))
    ax.axis("off")
    table = ax.table(cellText=rows, colLabels=headers, cellLoc="center", loc="center")
    table.auto_set_font_size(False)
    table.set_fontsize(8.5)
    table.scale(1, 1.55)

    for j in range(len(headers)):
        cell = table[(0, j)]
        cell.set_facecolor("#1a3a6b")
        cell.set_text_props(color="white", fontweight="bold")

    for i in range(1, len(rows) + 1):
        for j in range(len(headers)):
            table[(i, j)].set_facecolor("#f7f9fc")
        table[(i, 0)].set_text_props(fontweight="bold")
        for j in [3, 6, 9]:
            value = float(rows[i - 1][j])
            table[(i, j)].set_text_props(
                color="#006400" if value >= 0 else "#b22222",
                fontweight="bold",
            )

    ax.set_title(
        "Original Benchmark Reproduction Summary\n"
        "Paper values are from Anomaly Transformer ICLR 2022 Table 1",
        loc="left",
        fontsize=10,
        pad=8,
    )
    plt.tight_layout()
    save("fig4_original_results_table")


if __name__ == "__main__":
    print("=" * 68)
    print("Generating original benchmark comparison figures")
    print("Output folder: original_benchmark_comparison/")
    print("=" * 68)

    write_comparison_csv()
    fig1_f1_comparison()
    fig2_metric_deltas()
    fig3_full_metric_comparison()
    fig4_results_table()

    print("=" * 68)
    print("Done. 4 figures plus CSV saved in original_benchmark_comparison/")
    print("=" * 68)
